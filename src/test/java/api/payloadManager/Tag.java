package api.payloadManager;

public class Tag {
	
	private String id;
	private String name;




	public String getId() 
	{
		return id;
	}

	public void setId(String tagId) 
	{
		this.id = tagId;
	}

	public String getName() 
	{
		return name;
	}

	public void setName(String name) 
	{
		this.name = name;
	}

}
