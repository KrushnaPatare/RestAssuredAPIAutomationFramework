package api.payloadManager;

public class Category {
	
	private String id;
	private String name;


	

	public String getId() 
	{
		return id;
	}

	public void setId(String categoryId) 
	{
		this.id = categoryId;
	}

	public String getName() 
	{
		return name;
	}

	public void setName(String name) 
	{
		this.name = name;
	}
	

}
